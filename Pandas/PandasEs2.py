import numpy as np # type: ignore

# Creazione di un array NumPy
arr = np.array([1, 2, 3, 4, 5])

# Esecuzione di operazioni element-wise
squared = arr ** 2  # Ogni elemento viene elevato al quadrato

# Calcolo della somma e media dell'array
somma = np.sum(arr)
media = np.mean(arr)

print(f"Array: {arr}")
print(f"Array al quadrato: {squared}")
print(f"Somma: {somma}, Media: {media}")



